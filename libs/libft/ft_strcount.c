#include "libft.h"

int	ft_strcount(const char *s, int c)
{
	size_t	i;
	int			count;

	i = 0;
	count = 0;
	while (s[i])
	{
		if (s[i] == c)
			count++;
		i++;
	}
	return (count);
}
