#include "libft.h"

void	ft_lstclear(t_list **lst, void (*del)(void *))
{
	t_list	*to_free;
	t_list	*next;

	if (!(*lst))
		return ;
	to_free = *lst;
	while (to_free)
	{
		next = to_free->next;
		(*del)(to_free->content);
		free(to_free);
		to_free = next;
	}
	*lst = NULL;
}
