#include "libft.h"

void	*ft_memcpy(void *dest, const void *src, size_t n)
{
	unsigned char		*a;
	const unsigned char	*b;

	if (!n || dest == src)
		return ((unsigned char *)dest);
	a = (unsigned char *)dest;
	b = (unsigned char *)src;
	while (n-- > 0)
		*(a++) = *(b++);
	return (dest);
}
