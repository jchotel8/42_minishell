#include "libft.h"

size_t	ft_strlcpy(char *dest, char *src, size_t size)
{
	size_t	i;
	size_t	count;

	count = 0;
	i = 0;
	if (!dest || !src)
		return (-1);
	count = ft_strlen(src);
	if (!size)
		return (count);
	while (src[i] && i < size - 1)
	{
		dest[i] = src[i];
		i++;
	}
	dest[i] = '\0';
	return (count);
}
